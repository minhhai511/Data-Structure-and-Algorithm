package LinkedList;

/**
 *
 * @author lephu
 */
public class Node <T> {
    private T data;
    private Node next;//pointer giu dia chi node tiep theo trong danh sach

    public Node(T data) {
        this.data = data;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
